//Declare variables using `var` for different data types in JavaScript, and verify their types using `typeof`,
//including an uninitialized variable. 

var firstname='harsha'
console.log(firstname)           // String
console.log(typeof(firstname))   // typeof is used to determine the datatype of the given value.

var companyName='testleaf'
console.log(companyName)
console.log(typeof(companyName))    // String

var mobileNumber = 9954623185
console.log(mobileNumber)
console.log(typeof(mobileNumber))   // integer

var isAutomation = true
console.log(isAutomation)
console.log(typeof(isAutomation))   // boolean

var hasPlaywright
console.log(hasPlaywright)
console.log(typeof(hasPlaywright))  // undefined

var Playwright=null
console.log(Playwright)
console.log(typeof(Playwright))  // it’s a historical bug in JavaScript.

